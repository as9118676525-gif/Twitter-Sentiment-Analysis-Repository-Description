from textblob import TextBlob
import matplotlib.pyplot as plt

print("ðŸ¤– AryanBot Sentiment Analyzer Ready!\n")

# --- Predefined demo sentences (aapko input dene ki zarurat nahi) ---
texts = [
    "I love this internship, it is amazing!",
    "The project is very difficult.",
    "I am feeling okay today.",
    "This AI code is fantastic and powerful.",
    "I hate when errors come again and again."
]

# --- Sentiment counters ---
positive = 0
negative = 0
neutral = 0

# --- Sentiment analysis ---
for text in texts:
    blob = TextBlob(text)
    polarity = blob.sentiment.polarity

    if polarity > 0:
        sentiment = "Positive ðŸ˜€"
        positive += 1
    elif polarity < 0:
        sentiment = "Negative ðŸ˜¡"
        negative += 1
    else:
        sentiment = "Neutral ðŸ˜"
        neutral += 1

    print(f"ðŸ“ Sentence: {text}")
    print(f"âž¡ï¸ Detected Sentiment: {sentiment}\n")

# --- Visualization (Graph) ---
labels = ['Positive ðŸ˜€', 'Negative ðŸ˜¡', 'Neutral ðŸ˜']
values = [positive, negative, neutral]

plt.bar(labels, values, color=['green','red','blue'])
plt.title("Sentiment Analysis Result")
plt.xlabel("Sentiment Type")
plt.ylabel("Count")
plt.show()

